# Meeting types for Scrum at Scale - using Literal types as in prototype
from enum import Enum

class SASMeetingType(str, Enum):
    EAT = "executive_action_team"
    EMS = "executive_metascrum"
    SOS = "scrum_of_scrums"
    SOSOS = "scrum_of_scrum_of_scrums"
